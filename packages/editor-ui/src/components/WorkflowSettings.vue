							</el-option>
						</el-select>
					</el-col>
				</el-row>
				<el-row>
					<el-col :span="10" class="setting-name">
						{{ $translateBase('workflowSettings.timezone') }}:
						<el-tooltip class="setting-info" placement="top" effect="light">
							<div slot="content" v-html="helpTexts.timezone"></div>
							<font-awesome-icon icon="question-circle" />
						</el-tooltip>
					</el-col>
					<el-col :span="14" class="ignore-key-press">
						<el-select v-model="workflowSettings.timezone" placeholder="Select Timezone" size="small" filterable>
							<el-option
								v-for="timezone of timezones"
								:key="timezone.key"
								:label="timezone.value"
								:value="timezone.key">
							</el-option>
						</el-select>
					</el-col>
				</el-row>
				<el-row>
					<el-col :span="10" class="setting-name">
						{{ $translateBase('workflowSettings.saveDataErrorExecution') }}:
						<el-tooltip class="setting-info" placement="top" effect="light">
							<div slot="content" v-html="helpTexts.saveDataErrorExecution"></div>
							<font-awesome-icon icon="question-circle" />
						</el-tooltip>
					</el-col>
					<el-col :span="14" class="ignore-key-press">
						<el-select v-model="workflowSettings.saveDataErrorExecution" placeholder="Select Option" size="small" filterable>
							<el-option
								v-for="option of saveDataErrorExecutionOptions"
								:key="option.key"
								:label="option.value"
								:value="option.key">
							</el-option>
						</el-select>
					</el-col>
				</el-row>
				<el-row>
					<el-col :span="10" class="setting-name">
						{{ $translateBase('workflowSettings.saveDataSuccessExecution') }}::
						<el-tooltip class="setting-info" placement="top" effect="light">
							<div slot="content" v-html="helpTexts.saveDataSuccessExecution"></div>
							<font-awesome-icon icon="question-circle" />
						</el-tooltip>
					</el-col>
					<el-col :span="14" class="ignore-key-press">
						<el-select v-model="workflowSettings.saveDataSuccessExecution" placeholder="Select Option" size="small" filterable>
							<el-option
								v-for="option of saveDataSuccessExecutionOptions"
								:key="option.key"
								:label="option.value"
								:value="option.key">
							</el-option>
						</el-select>
					</el-col>
				</el-row>
				<el-row>
					<el-col :span="10" class="setting-name">
						{{ $translateBase('workflowSettings.saveManualExecutions') }}::
						<el-tooltip class="setting-info" placement="top" effect="light">
							<div slot="content" v-html="helpTexts.saveManualExecutions"></div>
							<font-awesome-icon icon="question-circle" />
						</el-tooltip>
					</el-col>
					<el-col :span="14" class="ignore-key-press">
						<el-select v-model="workflowSettings.saveManualExecutions" placeholder="Select Option" size="small" filterable>
							<el-option
								v-for="option of saveManualOptions"
								:key="option.key"
								:label="option.value"
								:value="option.key">
							</el-option>
						</el-select>
					</el-col>
				</el-row>
				<el-row>
					<el-col :span="10" class="setting-name">
						{{ $translateBase('workflowSettings.saveExecutionProgress') }}:
						<el-tooltip class="setting-info" placement="top" effect="light">
							<div slot="content" v-html="helpTexts.saveExecutionProgress"></div>
							<font-awesome-icon icon="question-circle" />
						</el-tooltip>
					</el-col>
					<el-col :span="14" class="ignore-key-press">
						<el-select v-model="workflowSettings.saveExecutionProgress" placeholder="Select Option" size="small" filterable>
							<el-option
								v-for="option of saveExecutionProgressOptions"
								:key="option.key"
								:label="option.value"
								:value="option.key">
							</el-option>
						</el-select>
					</el-col>
				</el-row>
				<el-row>
					<el-col :span="10" class="setting-name">
						{{ $translateBase('workflowSettings.timeoutWorkflow') }}:
						<el-tooltip class="setting-info" placement="top" effect="light">
							<div slot="content" v-html="helpTexts.executionTimeoutToggle"></div>
							<font-awesome-icon icon="question-circle" />
						</el-tooltip>
					</el-col>
					<el-col :span="14">
						<div>
							<el-switch ref="inputField" :value="workflowSettings.executionTimeout > -1" @change="toggleTimeout" active-color="#13ce66"></el-switch>
							<div class="expression-info clickable" @click="expressionEditDialogVisible = true">{{ $translateBase('workflowSettings.editExpression') }}</div>
						</div>
					</el-col>
				</el-row>
				<div v-if="workflowSettings.executionTimeout > -1">
					<el-row>
						<el-col :span="10" class="setting-name">
							{{ $translateBase('workflowSettings.timeoutAfter') }}:
							<el-tooltip class="setting-info" placement="top" effect="light">
								<div slot="content" v-html="helpTexts.executionTimeout"></div>
								<font-awesome-icon icon="question-circle" />
							</el-tooltip>
						</el-col>
						<el-col :span="4">
							<el-input-number size="small" v-model="timeoutHMS.hours" :min="0" :placeholder="$translateBase('workflowSettings.hours')" type="number" class="el-input_inner"></el-input-number><br />
							<div class="timeout-setting-name">{{ $translateBase('workflowSettings.hours') }}</div>
						</el-col>
						<el-col :span="4">
							<el-input-number size="small" v-model="timeoutHMS.minutes" :min="0" :placeholder="$translateBase('workflowSettings.minutes')" type="number" class="el-input_inner"></el-input-number><br />
							<div class="timeout-setting-name">{{ $translateBase('workflowSettings.minutes') }}</div>
						</el-col>
						<el-col :span="4">
							<el-input-number size="small" v-model="timeoutHMS.seconds" :min="0" :placeholder="$translateBase('workflowSettings.seconds')" type="number" class="el-input_inner"></el-input-number><br />
							<div class="timeout-setting-name">{{ $translateBase('workflowSettings.seconds') }}</div>
						</el-col>
					</el-row>
				</div>
				<div class="action-buttons">
					<el-button type="success" @click="saveSettings">
						{{ $translateBase('workflowSettings.save') }}
					</el-button>
				</div>
			</div>
		</el-dialog>
	</span>
</template>

<script lang="ts">
import Vue from 'vue';

import { externalHooks } from '@/components/mixins/externalHooks';
import { restApi } from '@/components/mixins/restApi';
import { genericHelpers } from '@/components/mixins/genericHelpers';
import { showMessage } from '@/components/mixins/showMessage';
import {
	ITimeoutHMS,
	IWorkflowDataUpdate,
	IWorkflowSettings,
	IWorkflowShortResponse,
} from '@/Interface';

import mixins from 'vue-typed-mixins';

export default mixins(
	externalHooks,
	genericHelpers,
	restApi,
	showMessage,
).extend({
	name: 'WorkflowSettings',
	props: [
		'dialogVisible',
	],
	data () {
		return {
			isLoading: true,
			helpTexts: {
				errorWorkflow: this.$translateBase('workflowSettings.helpTexts.errorWorkflow'),
				timezone: this.$translateBase('workflowSettings.helpTexts.timezone'),
				saveDataErrorExecution: this.$translateBase('workflowSettings.helpTexts.saveDataErrorExecution'),
				saveDataSuccessExecution: this.$translateBase('workflowSettings.helpTexts.saveDataSuccessExecution'),
				saveExecutionProgress: this.$translateBase('workflowSettings.helpTexts.saveExecutionProgress'),
				saveManualExecutions: this.$translateBase('workflowSettings.helpTexts.saveManualExecutions'),
				executionTimeoutToggle: this.$translateBase('workflowSettings.helpTexts.executionTimeoutToggle'),
				executionTimeout: this.$translateBase('workflowSettings.helpTexts.executionTimeout'),
			},
			defaultValues: {
				timezone: 'America/New_York',
				saveDataErrorExecution: 'all',
				saveDataSuccessExecution: 'all',
				saveExecutionProgress: false,
				saveManualExecutions: false,
			},
			saveDataErrorExecutionOptions: [] as Array<{ key: string, value: string }>,
			saveDataSuccessExecutionOptions: [] as Array<{ key: string, value: string }>,
			saveExecutionProgressOptions: [] as Array<{ key: string | boolean, value: string }>,
			saveManualOptions: [] as Array<{ key: string | boolean, value: string }>,
			timezones: [] as Array<{ key: string, value: string }>,
			workflowSettings: {} as IWorkflowSettings,
			workflows: [] as IWorkflowShortResponse[],
			executionTimeout: this.$store.getters.executionTimeout,
			maxExecutionTimeout: this.$store.getters.maxExecutionTimeout,
			timeoutHMS: { hours: 0, minutes: 0, seconds: 0 } as ITimeoutHMS,
		};
	},
	watch: {
		dialogVisible (newValue, oldValue) {
			if (newValue) {
				this.openDialog();
			}
			this.$externalHooks().run('workflowSettings.dialogVisibleChanged', { dialogVisible: newValue });
		},
	},
	methods: {
		closeDialog () {
			// Handle the close externally as the visible parameter is an external prop
			// and is so not allowed to be changed here.
			this.$emit('closeDialog');
			return false;
		},
		async loadSaveDataErrorExecutionOptions () {
			this.saveDataErrorExecutionOptions.length = 0;
			this.saveDataErrorExecutionOptions.push.apply( // eslint-disable-line no-useless-call
				this.saveDataErrorExecutionOptions, [
					{
						key: 'DEFAULT',
						value: this.$translateBase('workflowSettings.saveDataErrorExecutionOptions.defaultSave'),
					},
					{
						key: 'all',
						value: this.$translateBase('workflowSettings.saveDataErrorExecutionOptions.save'),
					},
					{
						key: 'none',
						value: this.$translateBase('workflowSettings.saveDataErrorExecutionOptions.doNotSave'),
					},
				],
			);
		},
		async loadSaveDataSuccessExecutionOptions () {
			this.saveDataSuccessExecutionOptions.length = 0;
			this.saveDataSuccessExecutionOptions.push.apply( // eslint-disable-line no-useless-call
				this.saveDataSuccessExecutionOptions, [
					{
						key: 'DEFAULT',
						value: this.$translateBase('workflowSettings.saveDataSuccessExecutionOptions.defaultSave'),
					},
					{
						key: 'all',
						value: this.$translateBase('workflowSettings.saveDataSuccessExecutionOptions.save'),
					},
					{
						key: 'none',
						value: this.$translateBase('workflowSettings.saveDataSuccessExecutionOptions.doNotSave'),
					},
				],
			);
		},
		async loadSaveExecutionProgressOptions () {
			this.saveExecutionProgressOptions.length = 0;
			this.saveExecutionProgressOptions.push.apply( // eslint-disable-line no-useless-call
				this.saveExecutionProgressOptions, [
					{
						key: 'DEFAULT',
						value: this.$translateBase('workflowSettings.saveExecutionProgressOptions.defaultSave'),
					},
					{
						key: true,
						value: this.$translateBase('workflowSettings.saveExecutionProgressOptions.yes'),
					},
					{
						key: false,
						value: this.$translateBase('workflowSettings.saveExecutionProgressOptions.no'),
					},
				],
			);
		},
		async loadSaveManualOptions () {
			this.saveManualOptions.length = 0;
			this.saveManualOptions.push({
				key: 'DEFAULT',
				value: this.$translateBase('workflowSettings.saveManualOptions.defaultSave'),
			});
			this.saveManualOptions.push({
				key: true,
				value: this.$translateBase('workflowSettings.saveManualOptions.yes'),
			});
			this.saveManualOptions.push({
				key: false,
				value: this.$translateBase('workflowSettings.saveManualOptions.no'),
			});
		},

		async loadTimezones () {
			if (this.timezones.length !== 0) {
				// Data got already loaded
				return;
			}

			const timezones = await this.restApi().getTimezones();

			let defaultTimezoneValue = timezones[this.defaultValues.timezone] as string | undefined;
			if (defaultTimezoneValue === undefined) {
				defaultTimezoneValue = 'Default Timezone not valid!';
			}

			this.timezones.push({
				key: 'DEFAULT',
				value: `Default - ${defaultTimezoneValue}`,
			});
			for (const timezone of Object.keys(timezones)) {
				this.timezones.push({
					key: timezone,
					value: timezones[timezone] as string,
				});
			}
		},
		async loadWorkflows () {
			const workflows = await this.restApi().getWorkflows();
			workflows.sort((a, b) => {
				if (a.name.toLowerCase() < b.name.toLowerCase()) {
					return -1;
				}
				if (a.name.toLowerCase() > b.name.toLowerCase()) {
					return 1;
				}
				return 0;
			});

			// @ts-ignore
			workflows.unshift({
				id: undefined as unknown as string,
				name: this.$translateBase('workflowSettings.noWorkflow'),
			});

			Vue.set(this, 'workflows', workflows);
		},
		async openDialog () {
			const workflowId = this.$route.params.name;
			if (this.$route.params.name === undefined) {
				this.$showMessage({
					title: this.$translateBase('workflowSettings.showMessage.openDialog.title'),
					message: this.$translateBase('workflowSettings.showMessage.openDialog.message'),
					type: 'error',
					duration: 0,
				});
				this.closeDialog();
				return;
			}

			this.defaultValues.saveDataErrorExecution = this.$store.getters.saveDataErrorExecution;
			this.defaultValues.saveDataSuccessExecution = this.$store.getters.saveDataSuccessExecution;
			this.defaultValues.saveManualExecutions = this.$store.getters.saveManualExecutions;
			this.defaultValues.timezone = this.$store.getters.timezone;

			this.isLoading = true;
			const promises = [];
			promises.push(this.loadWorkflows());
			promises.push(this.loadSaveDataErrorExecutionOptions());
			promises.push(this.loadSaveDataSuccessExecutionOptions());
			promises.push(this.loadSaveExecutionProgressOptions());
			promises.push(this.loadSaveManualOptions());
			promises.push(this.loadTimezones());

			try {
				await Promise.all(promises);
			} catch (error) {
				this.$showError(
					error,
					this.$translateBase('workflowSettings.showError.openDialog.title'),
					this.$translateBase('workflowSettings.showError.openDialog.message', { colon: true }),
				);
			}

			const workflowSettings = JSON.parse(JSON.stringify(this.$store.getters.workflowSettings));

			if (workflowSettings.timezone === undefined) {
				workflowSettings.timezone = 'DEFAULT';
			}
			if (workflowSettings.saveDataErrorExecution === undefined) {
				workflowSettings.saveDataErrorExecution = 'DEFAULT';
			}
			if (workflowSettings.saveDataSuccessExecution === undefined) {
				workflowSettings.saveDataSuccessExecution = 'DEFAULT';
			}
			if (workflowSettings.saveExecutionProgress === undefined) {
				workflowSettings.saveExecutionProgress = 'DEFAULT';
			}
			if (workflowSettings.saveManualExecutions === undefined) {
				workflowSettings.saveManualExecutions = 'DEFAULT';
			}
			if (workflowSettings.executionTimeout === undefined) {
				workflowSettings.executionTimeout = this.$store.getters.executionTimeout;
			}
			if (workflowSettings.maxExecutionTimeout === undefined) {
				workflowSettings.maxExecutionTimeout = this.$store.getters.maxExecutionTimeout;
			}

			Vue.set(this, 'workflowSettings', workflowSettings);
			this.timeoutHMS = this.convertToHMS(workflowSettings.executionTimeout);
			this.isLoading = false;
		},
		async saveSettings () {
			// Set that the active state should be changed
			const data: IWorkflowDataUpdate = {
				settings: this.workflowSettings,
			};

			// Convert hours, minutes, seconds into seconds for the workflow timeout
			const { hours, minutes, seconds } = this.timeoutHMS;
			data.settings!.executionTimeout =
				data.settings!.executionTimeout !== -1
					? hours * 3600 + minutes * 60 + seconds
					: -1;

			if (data.settings!.executionTimeout === 0) {
				this.$showError(
					new Error(this.$translateBase('workflowSettings.showError.saveSettings1.errorMessage')),
					this.$translateBase('workflowSettings.showError.saveSettings1.title'),
					this.$translateBase('workflowSettings.showError.saveSettings1.message', { colon: true }),
				);
				return;
			}

			// @ts-ignore
			if (data.settings!.executionTimeout > this.workflowSettings.maxExecutionTimeout) {
				const { hours, minutes, seconds } = this.convertToHMS(this.workflowSettings.maxExecutionTimeout as number);
				this.$showError(
					new Error(this.$translateBase('workflowSettings.showError.saveSettings2.errorMessage')),
					this.$translateBase('workflowSettings.showError.saveSettings2.title'),
					this.$translateBase('workflowSettings.showError.saveSettings2.message', { colon: true }),
				);
				return;
			}
			delete data.settings!.maxExecutionTimeout;

			this.isLoading = true;

			try {
				await this.restApi().updateWorkflow(this.$route.params.name, data);
			} catch (error) {
				this.$showError(
					error,
					this.$translateBase('workflowSettings.showError.saveSettings3.title'),
					this.$translateBase('workflowSettings.showError.saveSettings3.message', { colon: true }),
				);
				this.isLoading = false;
				return;
			}

			// Get the settings without the defaults set for local workflow settings
			const localWorkflowSettings: IWorkflowSettings = {};
			for (const key of Object.keys(this.workflowSettings)) {
				if (this.workflowSettings[key] !== 'DEFAULT') {
					localWorkflowSettings[key] = this.workflowSettings[key];
				}
			}

			const oldSettings = JSON.parse(JSON.stringify(this.$store.getters.workflowSettings));

			this.$store.commit('setWorkflowSettings', localWorkflowSettings);

			this.isLoading = false;

			this.$showMessage({
				title: this.$translateBase('workflowSettings.showMessage.saveSettings.title'),
				message: this.$translateBase('workflowSettings.showMessage.saveSettings.message'),
				type: 'success',
			});

			this.closeDialog();

			this.$externalHooks().run('workflowSettings.saveSettings', { oldSettings });
		},
		toggleTimeout() {
			this.workflowSettings.executionTimeout = this.workflowSettings.executionTimeout === -1 ? 0 : -1;
		},
		convertToHMS(num: number): ITimeoutHMS {
			if (num > 0) {
				let remainder: number;
				const hours = Math.floor(num / 3600);
				remainder = num % 3600;
				const minutes = Math.floor(remainder / 60);
				const seconds = remainder % 60;
				return { hours, minutes, seconds };
			}
			return { hours: 0, minutes: 0, seconds: 0 };
		},
	},
});
</script>

